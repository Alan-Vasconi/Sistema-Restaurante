    private void txtPagoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPagoKeyPressed
        int keyCode = evt.getKeyCode();
        
        switch (keyCode) {
        case KeyEvent.VK_F2 -> {
            FrmVendas frmVendas = new FrmVendas();
            frmVendas.setVisible(true);
            this.dispose();
            }
            
        case KeyEvent.VK_F4 -> {
            FrmFinalizar frmFinalizar = new FrmFinalizar();
            frmFinalizar.setVisible(true);
            this.dispose();
            }
        default -> {
            }
        }
        
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try{
            float troco = Float.parseFloat(txtTroco.getText().replace(",", "."));
            if(troco < 0){
                float trocopositivo = Math.abs(troco); 
                String mensagem = String.format("Pagamento Inválido! Troco negativo, falta: (%.2f)", trocopositivo);
                txtMensagem.setText(mensagem);
            }else{
                txtMensagem.setText("Pagamento Efetuado no Dinheiro com sucesso!");
                jLabel6.requestFocus();
            }
            evt.consume();          
            } catch (NumberFormatException ex) {
                txtMensagem.setText("Formato inválido para o troco");
            }
       }
    }//GEN-LAST:event_txtPagoKeyPressed

