    private void calcularTroco() {
        try {
            float totalpago = Float.parseFloat(txtPago.getText().replace(",", "."));
            float totalvenda = Float.parseFloat(txtTotal.getText().replace(",", "."));

            float troco = totalpago - totalvenda;
            String trocoFormatado = String.format("%.2f", troco); 
            txtTroco.setText(trocoFormatado);
        } catch (NumberFormatException e) {
            
            txtTroco.setText("Formato inválido");
        }
    }
    
